// hide and show nav block title
function navAppear( headerId ) { 
    $( headerId ).addClass('hide');
}
function navHide( headerId ) {  
    $( headerId ).removeClass('hide');
}

// hide and show nav block title
function listAppear( navId ) {  
    $( navId ).removeClass('hide');
}
function listHide( navId ) {  
    $( navId ).addClass('hide');
}

